#include "libft.h"

int	ft_atoi(const char *s)
{
	int		i;
	int		ng;
	long	res;

	i = 0;
	ng = 1;
	res = 0;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\t'
		|| s[i] == '\r' || s[i] == '\v' || s[i] == '\f')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			ng = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = (res * 10) + (s[i] - '0');
		if ((res * ng < -2147483648) || (res * ng > 2147483648))
			return ((res * ng < -2147483648) - 1);
		i++;
	}
	return (res * ng);
}
