#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	num;

	if (n < 0)
	{
		num = (unsigned int)(n * (-1));
		write(fd, "-", 1);
	}
	else
		num = (unsigned int)n;
	if (num > 9)
	{
		ft_putnbr_fd(num / 10, fd);
		ft_putnbr_fd(num % 10, fd);
	}
	else
	{
		num = (num + '0');
		write(fd, &num, 1);
	}
}
